                        <p>A concentração de terra se amplia, e o plantio nesses imensos latifúndios, com aplicação de adubo, corretivos de solo e agrotóxico, exige maquinários pesados. A agricultura tornou-se agronegócio, viável apenas para as grandes empresas. Esse modelo é disseminado no mundo todo, e mais uma vez junto com a falsa promessa de acabar com a fome mundial, de levar o “desenvolvimento” aos países do chamado “Terceiro Mundo”. Denominando-a de <strong>Revolução Verde</strong>, o grande capital promoveu na verdade uma recolonização dos países do hemisfério sul e de parte da Ásia.</p>

                        <p>Vivemos então, nos últimos 50 anos, um agravamento dos efeitos colaterais já citados. À dimensão social do êxodo rural, da concentração de bens, da precarização da saúde e bem-estar, da destruição das culturas e formas de vida, podemos inserir as questões ambientais em proporções planetárias colocando em risco, agora não mais uma civilização, ou alguns povos, mas a própria espécie humana.</p>

                        <p>No Brasil, essa concentração é ainda mais violenta. Fruto da colonização que dividiu as terras indígenas em capitanias hereditárias, que se preservam até hoje na forma dos grandes latifúndios, essa imensa concentração de terra não sacia a ganância dos ruralistas que avançam ainda mais sobre as terras indígenas e quilombolas, últimos depositários de florestas e biodiversidade.</p>

                        <p>O êxodo rural provoca o inchaço das grandes cidades formando grandes bolsões de miséria de domicílios de construção precária, sem infraestrutura de saneamento ou água potável, que avançam sobre rios, mangues, lagoas, encostas dos morros, dunas, pondo em risco a vida de humanos e degradando ainda mais o que a cidade ainda não destruiu do meio ambiente.</p>

                        <p>Esses grandes latifúndios são os principais desflorestadores, destruindo, compactando, envenenando o solo para o plantio de suas monoculturas (soja, milho, cana de açúcar, etc) e a pecuária. Milhões de hectares que se inviabilizam em poucos anos para a agricultura, exigindo o avanço sobre as florestas. Os biomas do Cerrado e da Caatinga já estão com quase 50% de suas áreas degradas. A floresta Amazônica está quase chegando ao ponto que impedirá sua possibilidade de recuperação. A Mata Atlântica tornou-se manchas que sobrevivem sobre constante ameaça. E é bom deixar claro aqui que, a mesma conversa usada no passado de que essa destruição é motivada pela necessidade de alimentar a Nação, é uma grande falácia. O agronegócio produz ração animal e biocombustível para exportação. E é inviável economicamente. Não se manteria por si só, não fosse os altos subsídios financiados pelos governos e a forte pressão da frente parlamentar ruralista.</p>

