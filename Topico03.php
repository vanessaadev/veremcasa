                        <p>É importante lembrarmos aqui que o domínio da agricultura, do plantar e do colher, do produzir alimento, gerou abundância e riqueza, permitindo o desenvolvimento das grandes cidades e civilizações. O excedente de grãos passou então a ser comercializado. A princípio, por meio de trocas, depois, com a invenção da moeda, se estabelecia o comércio e o enriquecimento daqueles que dominavam as técnicas de produção. Junto com a riqueza vieram as armas para protegê-las. A defesa de territórios do plantio. Estabelecia-se assim o comércio e as guerras. Os homens que lutavam pela sobrevivência espalhando-se pela Terra, lutam agora por terra e poder.</p>

                        <p>Entre o século XVI (anos dos 1500) e o século XIX (anos dos 1800), os humanos que viviam na Europa, passam por uma série de mudanças na sua forma de ser, estar e ver o mundo. Vimos nos livros de história, (porque a história que estão nos livros da escola são a história desses homens europeus) que, após as grandes navegações de conquista sobre os povos que viviam na África e nas Américas, após lhes saquearem as riquezas, escravizarem ou exterminarem suas gentes, devastarem sua natureza para criar enormes plantações, esses homens que enriqueceram mais e mais, desenvolveram enormemente a suas tecnologias e ciências. Criaram máquinas a vapor e dominaram a energia elétrica. Sua agricultura, porém, ainda permanecia manual, com tração humana ou animal para a preparação do solo, e também manualmente semeavam e colhiam.</p>

                        <p>No final dos anos dos 1800, século XIX, com o desenvolvimento da química principalmente, a ciência começasse a aplicar na produção de alimentos seus conhecimentos. A partir de diversos experiências, foi demonstrado a dependência das plantas de determinados elementos químicos, divididos entre macronutrientes (aqueles que as plantas precisam em maior quantidade) e micronutrientes (que as plantas precisam em quantidades muito pequenas). Após estudos sobre quantidades e formas de aplicação, esses “adubos químicos” passam a ser aplicados na agricultura. Cabe aqui mais uma vez lembrar: A agricultura já não é apenas uma técnica de produção de alimento, mas um negócio de acumulação de riquezas e poder. E o domínio das técnicas e meios de produção significa controle da riqueza e do poder.</p>

                        <p>Outro fator que vai modificar ainda mais a forma de produzir alimento, de se fazer agricultura, é o desenvolvimento do conhecimento da genética. A partir dos anos 30, já no século XX, os laboratórios passam a produzir sementes híbridas, modificadas geneticamente. Sementes híbridas e adubação química: está posto o reflexo da revolução industrial na agricultura. O aumento da produção impulsiona a produção em larga escala. O ser humano estará livre do fantasma da fome, dizem.</p>

                        <p>Dois efeitos colaterais, porém, foram sentidos de imediato. Um de ordem social e outro referente à própria produção agrícola. Acontece que as sementes híbridas produzem bem na primeira safra, mas como tem uma taxa de germinação muito pequena para que se selecione as melhores sementes para a safra seguinte, ela quebra o processo tradicional de guarda de sementes como se fazia anteriormente. Cria-se assim uma dependência do agricultor junto aos laboratórios produtores de semente e adubo químico. O capital financeiro assenta-se sobre a produção agrícola inviabilizando a vida do pequeno agricultor. Muitos abandonam o campo provocando uma grande migração para os grandes centros urbanos, onde venderão sua força de trabalho para os serviços da industria que está em ascensão.</p>

                        <p>Outro efeito colateral é que as sementes híbridas precisam de elevada adubação nitrogenada e em função desse bombardeio de Nitrogênio ficam sujeitas à uma alta incidência de insetos e fungos. Mais uma vez entram em campo os laboratórios. Sem que se questione os motivos do adoecimento das culturas, criam os agrotóxicos para combater as ditas pragas das plantações. Está posto a tríade da agricultura promovida pelo capital: <strong>sementes modificadas em laboratórios + adubação química + agrotóxicos</strong>.</p>

